package xyz.vjudge.contest325153;

import java.util.Scanner;

public class ProblemG {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int a,b;
        while (in.hasNext()) {
            a = in.nextInt();
            b = in.nextInt();
            System.out.println(a + b);
            System.out.println("");
        }
    }
}
